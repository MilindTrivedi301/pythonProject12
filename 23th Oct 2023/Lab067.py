

original_str = "SUMIT"
def rev_string(original_str):
    return ''.join(reversed(original_str))              #joined with an empty character''

rev_str = rev_string(original_str)
print(rev_str)

#----------------------------------------------------------------------------------------------

new_str = "AMIT"
def rev_string(new_str):
    return 'AB'.join(reversed(new_str))              #joined with an added character in the ''

rev_str = rev_string(new_str)
print(rev_str)